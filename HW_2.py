# Побитовые операции

a = 5
b = 6
print(f'Результат бинарного оператора "И": {a & b}')
print(f'Результат бинарного оператора "ИЛИ": {a | b}')
print(f'Результат бинарного оператора "ИСКЛЮЧАЮЩЕЕ ИЛИ": {a ^ b}')
print(f'Побитовый сдвиг влево числа {a}: {a << 2}')
print(f'Побитовый сдвиг вправо числа {a}: {a >> 2}')
print(f'Инверсия числа 5: {~a}')
